$(function(){
    $.post("/wx/wxconfig/",{
		"url":location.href
	},function(data){
		wx.config(data);
		wx.ready(function(){
			wx.onMenuShareTimeline({
                link:"http://football.qingdianer.com",
                imgUrl:"http://football.qingdianer.com/static/image/share-image.jpg",
                title:"呐喊吧！为中国队加油！",
			});
			wx.onMenuShareAppMessage({
                link:"http://football.qingdianer.com",
                imgUrl:"http://football.qingdianer.com/static/image/share-image.jpg",
                title:"呐喊吧！为中国队加油！",
			});
        });
		wx.error(function(res){
			$.get("/wx/update_access_token/",function(data){
				$.post("/wx/wxconfig/",{
					"url":location.href
				},function(data){
					wx.config(data);
					wx.ready(function(){
                        wx.onMenuShareTimeline({
                            link:"http://football.qingdianer.com",
                            imgUrl:"http://football.qingdianer.com/static/image/share-image.jpg",
                            title:"呐喊吧！为中国队加油！",
                        });
                        wx.onMenuShareAppMessage({
                            link:"http://football.qingdianer.com",
                            imgUrl:"http://football.qingdianer.com/static/image/share-image.jpg",
                            title:"呐喊吧！为中国队加油！",
                        });
		            });
		        });
		    });
        });
    });
});
